#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	num;

	sign = 1;
	num = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		if (num < 0)
		{
			if (sign == -1)
				return (0);
			return (-1);
		}
		num = num * 10 + (*str++ - 48);
	}
	return (num * sign);
}
